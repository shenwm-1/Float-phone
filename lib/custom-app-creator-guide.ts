  "",
  "**返回结构**：",
  "",
  "* `voice.tts` → `{ ok, configId, provider, voiceId, mimeType, size, dataUrl }`，`dataUrl` 可直接播放。",
  "* `voice.stt` → `{ ok, text, interim?, reason? }`。",
  "* `voice.readProfiles` → `{ selected, profiles, characterId? }`，每个 profile 含 `provider`/`model`，可用于判断当前语音支持的高级能力。",
  "",
