  "### 5.8 让角色生成文本",
  "",
  "`ai.generate` 按指定角色组装一次**完整提示词**，但只返回文本，不自动发到聊天室。它保留角色人设、用户人设、核心记忆、长期记忆、短期记忆事件流等基础上下文；传入 `appTags` 时还会加入匹配的 APP 预设条目（规则见 [第 4 节](#4-声明文件与角色生成链路)）。",
  "",
  "普通单轮任务只传 `instruction`；如果 APP 自己有多轮剧情、短信线程、客服对话等内部记录，把这些记录作为 `messages` 传入。宿主会把 `messages` 当作真正的 `user` / `assistant` 历史消息参与统一提示词组装，而不是把它们塞进一条说明文字里。",
  "",
  "`messages` 是 APP 自己的私有多轮历史；短期记忆事件流由宿主单独读取，里面包含该角色最近的聊天、群聊、其他 APP 事件、日记、朋友圈等。最终进入提示词时，宿主会把短期记忆事件流和 `messages` 按 `createdAt` / `timestamp` 合并排序，再放进 `<shortTermMemory>`。因此，多轮 APP 保存历史时应保留每条消息的 `createdAt`。",
  "",
  "```js",
  "const scene = await AiPhone.db.get(\"scenes\", sceneId);",
  "const turns = scene.turns || [];",
  "",
  "const result = await AiPhone.ai.generate({",
  "  characterId: selectedCharacterId,",
  "  appTags: [\"infinite_flow\", \"scene\"],",
  "  messages: turns.slice(-24).map(turn => ({",
  "    role: turn.role,",
  "    content: turn.content,",
  "    createdAt: turn.createdAt",
  "  })),",
  "  instruction: `",
  "当前副本摘要：",
  "${scene.summary || \"暂无\"}",
  "",
  "当前场景状态：",
  "${JSON.stringify(scene.state || {}, null, 2)}",
  "",
  "用户本轮行动：",
  "${userAction}",
  "",
  "请作为当前角色继续推进剧情，只输出角色这一轮的回复。",
  "`",
  "});",
  "console.log(result.text);",
  "",
  "await AiPhone.db.update(\"scenes\", sceneId, {",
  "  turns: [",
  "    ...turns,",
  "    { role: \"user\", content: userAction, createdAt: new Date().toISOString() },",
  "    ...result.appendMessages",
  "  ]",
  "});",
  "```",
  "",
  "* `messages` / `appMessages`：APP 自己维护的多轮记录，支持 `role: \"user\" | \"assistant\" | \"system\" | \"tool\"`、`content`、`createdAt`，也支持 `mediaType`、`rawResponseText`、`nativeToolCalls`、`nativeToolResult` 等工具上下文字段。不传时表示本 APP 没有私有多轮历史；宿主仍会通过短期记忆事件流补入该角色最近发生的聊天和其他 APP 事件。",
  "* `instruction`：本轮任务说明，适合放当前状态、用户本轮操作、输出要求。不要把几十轮历史都拼进 `instruction`；多轮历史应放 `messages`。",
  "* `result.text`：本轮工具循环结束后的最终可显示回复文本。",
  "* `result.appendMessages`：本轮生成新增的上下文消息，包含中间工具调用、工具结果和最终 assistant 回复。剧情 APP 应把它们追加到自己的多轮记录里；下一轮再作为 `messages` 原样传回。文本协议工具结果会以 `role: \"user\"` + `mediaType: \"tool_result\"` 保存；原生工具结果会以 `role: \"tool\"` + `nativeToolResult` 保存。",
  "* `historyLimit`：可选；当生成方案使用 `history: \"recent\"` 时，限制 `messages` / `appMessages` 只取最近多少条。通常由 APP 自己决定传多少轮历史。",
  "",
  "**多角色生成**：把 `characterId` 换成 `characterIds` 数组（或传一个已有群聊的 `sessionId`），一次生成里同台多位角色。心智模型和单角色完全一致：宿主组装\"多人资料包\"（每位角色的完整人设、状态、记忆、日程、世界书都进入上下文），场景任务由你的 `presets.json` 条目按 `appTags` 命中——宿主不会混入自己内置的聊天/群聊格式条目。**输出格式完全由你的场景条目约定，返回原始文本，由 APP 自行解析渲染**（想要谁说哪句，就在预设里规定台词标记格式）。",
  "",
  "```js",
  "const result = await AiPhone.ai.generate({",
  "  characterIds: [\"char_a\", \"char_b\", \"char_c\"],",
  "  appTags: [\"theater\", \"scene\"],",
  "  instruction: \"三人在天台对峙，按剧本推进这一幕，每人至少发言一次。\"",
  "});",
  "// result.text 是模型原始输出，按你在预设里约定的格式自行解析",
  "const lines = result.text.match(/\\[台词@[^\\]]+\\]/g);",
  "```",
  "",
  "* `characterIds`：参与生成的角色 id 数组。也可以改传 `sessionId`（已存在的群聊会话），此时自动带入该群最近的聊天记录作为上下文。",
  "* `result.text`：模型的原始输出文本，宿主不做任何按角色切分或格式清洗。",
  "* `result.appendMessages`：与单角色模式相同，用于多轮续写时作为 `messages` 传回。",
  "* 多角色模式同样支持 `messages`、`instruction`、`appTags`、`historyLimit` 和生成方案；暂不支持工具调用。",
  "",
  "需 `ai.generate`。",
  "",
  "不需要角色聊天链路、只想调用通用模型时，用 `ai.chat` / `ai.embed` / `ai.classify`：",
  "",
  "```js",
  "const chat = await AiPhone.ai.chat({",
  "  messages: [",
  "    { role: \"system\", content: \"你是简短分类助手。\" },",
  "    { role: \"user\", content: \"这条评论看起来像差评吗？\" }",
  "  ],",
  "  timeoutMs: 60000",
  "});",
  "const embedding = await AiPhone.ai.embed({ text: \"冬阴功汤很好喝\" });",
  "const cls = await AiPhone.ai.classify({ text: \"太辣了\", labels: [\"好评\", \"中评\", \"差评\"] });",
  "```",
  "",
  "`ai.chat` 的 `timeoutMs` 可选，不传时宿主不主动截断；传入后只影响本次请求。分别需 `ai.chat` / `ai.embed` / `ai.classify`。",
  "",
  "需要一张配图时用 `ai.generateImage`，它会复用用户在宿主设置里配置好的生图 API（APP 自己不需要管理生图 API Key）：",
  "",
  "```js",
  "const image = await AiPhone.ai.generateImage({",
  "  prompt: \"一张温暖的深夜街角便利店插画，暖黄灯光，雨后地面反光\",",
  "  characterId: selectedCharacterId,",
  "  useReferenceImage: true",
  "});",
  "if (image.dataUrl) imgEl.src = image.dataUrl;",
