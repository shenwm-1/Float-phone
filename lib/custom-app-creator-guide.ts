  "* “短信回复”预设：约束角色用短信语气回复，内容短一点，像真实手机短信，并约定前端可解析的输出格式。",
  "* “主动短信”预设：约束角色主动发消息时要有明确动机，不能像普通聊天一样无限展开，并约定前端可解析的输出格式。",
  "",
  "APP 调用 `AiPhone.ai.generate` 并传 `appTags: [\"sms\", \"reply\"]` 时，提示词为通用条目 + “短信回复”预设；传 `appTags: [\"sms\", \"proactive\"]` 时，提示词为通用条目 + “主动短信”预设。",
  "",
  "**输出后处理**：`ai.generate` 只返回文本。如果预设约定了角色输出某种固定标记（如 `[状态:地点=...;时间=...]`），APP 拿到 `result.text` 后直接在自己的 JS 里解析、渲染成卡片或状态栏即可，不需要向宿主注册任何规则。",
  "",
  "**世界书**：角色在绑定设置里配置的世界书会自动进入生成链路，APP 不需要做任何事。APP 自己的场景背景直接写进预设条目。",
  "",
