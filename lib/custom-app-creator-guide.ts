  "await AiPhone.tasks.schedule({",
  "  id: \"order_001_delivered\",",
  "  delayMs: 35 * 60 * 1000,",
  "  actions: [",
  "    { type: \"notification\", title: \"外卖到了\", body: \"你的冬阴功汤已经送达。\", badgeDelta: 1 },",
