  "需 `chat.requestReply`。`requestReply` 触发的是普通聊天室回复，和用户在聊天框发消息后的生成链路一致；它不接收 APP 场景 tags，也不会加载 APP 专属预设。角色能看到的是你刚刚写入的历史消息或卡片 `historyText`。如果像上例一样读取用户昵称，还需 `user.profile.read`。",
  "",
  "如果 APP 需要用自己的 APP 预设生成内容，请调用 `AiPhone.ai.generate`，然后用 `chat.writeHistory` 或 `chat.sendCard` 把结果写回聊天。",
  "",
  "### 5.15 打开角色聊天室",
  "",
  "```js",
  "await AiPhone.chat.openConversation({ characterId: selectedCharacterId });",
  "```",
  "",
  "### 5.16 修改聊天联系人状态",
  "",
  "可做拉黑、免打扰这类和聊天联动的 APP：",
  "",
  "```js",
  "await AiPhone.chat.setContactState({",
  "  characterId: selectedCharacterId, isBlacklisted: true, isMuted: true",
  "});",
  "await AiPhone.chat.unblock(selectedCharacterId);",
  "await AiPhone.chat.unmute(selectedCharacterId);",
  "```",
  "",
  "需 `chat.contacts.write`。",
  "",
  "### 5.17 读取聊天历史",
  "",
  "```js",
  "const current = await AiPhone.chat.getCurrentSession();",
  "const history = await AiPhone.chat.readHistory({ sessionId: current?.sessionId, limit: 30 });",
  "```",
  "",
  "需 `chat.read`。`getCurrentSession` **只有从聊天卡片 / + 号面板进入时**才返回会话信息，桌面直接打开时为 `null`。所以不要默认能拿到当前会话——桌面入口下，`sessionId`/`characterId` 应由用户在 APP 内自己选择（如先用 `characters.list` 让用户挑角色）。",
  "",
  "### 5.18 监听聊天事件",
  "",
  "可监听 manifest `extensions.events` 里声明过的事件。当前支持 `chat.message.created`，用于响应用户或角色的新消息。事件声明里写 `background: true` 时，即使 APP 没打开，宿主也会临时启动隐藏运行环境，执行同一段 `AiPhone.on` handler，执行完成后销毁；隐藏运行环境默认最多保留 5 分钟，确实需要更久时可在事件声明里写 `timeoutMs`，最高 30 分钟。",
  "",
  "```js",
  "const stop = AiPhone.on(\"chat.message.created\", async (payload) => {",
  "  const text = payload.message?.content || \"\";",
  "  if (payload.message?.role !== \"assistant\") return;",
  "  if (!text.includes(\"[漂流瓶:\")) return;",
  "",
  "  await AiPhone.db.create(\"bottles\", {",
  "    content: text,",
  "    characterId: payload.characterId,",
  "    messageId: payload.message.id,",
  "    createdAt: new Date().toISOString()",
  "  });",
  "});",
  "// 不再需要时：",
  "stop();",
  "```",
  "",
  "需在 `extensions.events` 声明该事件，并声明 `chat.read` 或 `chat.read.background` 权限。后台 handler 里的异步操作要 `await` 或返回 Promise，宿主会等待 Promise 完成；如果只启动异步任务但不返回 Promise，隐藏运行环境可能会提前销毁。后台 handler 写出的聊天消息会带 `custom_app_background` 来源，宿主不会再用这些消息触发后台监听，避免无限循环。",
  "",
  "### 5.19 读取记忆",
  "",
  "```js",
  "const core = await AiPhone.memory.readCore({ characterId: selectedCharacterId });",
  "const longTerm = await AiPhone.memory.readLongTerm({ characterId: selectedCharacterId, query: \"清淡口味\" });",
  "const shortTerm = await AiPhone.memory.readShortTerm({ characterId: selectedCharacterId, limit: 30 });",
  "const found = await AiPhone.memory.search({ characterId: selectedCharacterId, query: \"喜欢少辣\" });",
  "```",
  "",
  "分别需 `memory.readCore` / `memory.readLongTerm` / `memory.readShortTerm` / `memory.search`。",
  "",
  "### 5.20 写入记忆",
  "",
  "三种写法，按\"重要程度\"选：",
  "",
  "**`memory.add`** —— 写长期/核心记忆，**只在确实重要、未来需长期影响角色时用**。不要把每次点击都写成长期记忆。",
  "",
  "```js",
  "const userProfile = await AiPhone.user.getProfile({ characterId: selectedCharacterId });",
  "const userName = userProfile?.name || \"你\";",
  "",
  "await AiPhone.memory.add({",
  "  characterId: selectedCharacterId, type: \"long_term\",",
  "  content: `${userName}长期偏好清淡少辣的食物。`,",
  "  importance: 0.6, reason: \"外卖 APP 多次点单观察\"",
  "});",
  "```",
  "",
  "需 `memory.write`；如果像上例一样读取用户昵称，还需 `user.profile.read`。",
  "",
  "**`memory.suggest`** —— 只是建议写入、需用户或宿主之后确认，**不直接写进长期记忆**。",
  "",
  "```js",
  "const userProfile = await AiPhone.user.getProfile({ characterId: selectedCharacterId });",
  "const userName = userProfile?.name || \"你\";",
  "",
  "await AiPhone.memory.suggest({",
  "  characterId: selectedCharacterId, type: \"long_term\",",
  "  content: `${userName}喜欢清淡少辣的外卖。`, reason: \"多次点单观察\"",
  "});",
  "```",
  "",
  "需 `memory.suggest`；如果像上例一样读取用户昵称，还需 `user.profile.read`。",
  "",
  "**`memory.addTimeline`** —— 将自定义 APP 的事件写入该角色的全局短期记忆事件流。写入后，这条事件会被同一角色下的所有 APP 在 `AiPhone.ai.generate` 的短期记忆事件流里读到。",
  "",
  "注意：`chat.sendCard({ historyText })` 和 `chat.writeHistory` 作为聊天室历史的一部分，也会进入短期记忆事件流。因此，如果已经使用 `chat.sendCard` 或 `chat.writeHistory` 写入同一事件，就无需再重复调用 `memory.addTimeline`。",
  "",
  "```js",
  "const [character, userProfile] = await Promise.all([",
  "  AiPhone.characters.get(selectedCharacterId),",
  "  AiPhone.user.getProfile({ characterId: selectedCharacterId })",
  "]);",
  "const characterName = character?.name || \"对方\";",
  "const userName = userProfile?.name || \"你\";",
  "",
  "const timeline = await AiPhone.memory.addTimeline({",
  "  characterId: selectedCharacterId, appLabel: \"Float 外卖\",",
  "  detail: \"order_delivered\", summary: `${userName}给${characterName}点的冬阴功汤和椰汁饭已经送达。`,",
  "  appEventId: \"order_001\",",
  "  data: { orderId: \"order_001\" }",
  "});",
  "```",
  "",
  "如果这件事后来被撤销、订单取消，或用户删除了对应卡片，可以按同一个 `appEventId` 删除短期时间线；如果只想删某一条，也可以用 `addTimeline` 返回的 `entry.id`：",
  "",
  "```js",
  "await AiPhone.memory.deleteTimeline({",
  "  characterId: selectedCharacterId,",
  "  appEventId: \"order_001\"",
  "});",
  "",
  "await AiPhone.memory.deleteTimeline({ entryId: timeline.entry.id });",
  "```",
  "",
  "`addTimeline` / `deleteTimeline` 都需 `memory.write`；如果像上例一样读取角色名和用户昵称，还需 `characters.read`、`user.profile.read`。",
  "",
