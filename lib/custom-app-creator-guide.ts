  "",
  "> 后台任务里**每个 action 都是独立动作，不会自动继承外层 `appTags`**。`chat.reply` 只触发普通聊天室回复；如果需要 APP 场景预设，请在任务中先用 APP 自己的 `AiPhone.ai.generate` 生成内容，再写入聊天历史或卡片。",
  "",
  "**可用动作 type**：`notification`、`badge`、`chat.card`、`chat.history`、`chat.message`、`chat.reply`、`memory.add`、`memory.timeline`、`memory.deleteTimeline`、`memory.suggest`、`wallet.pay`、`chat.contact`、`calendar.write`。",
  "",
  "**动作字段速查**：",
