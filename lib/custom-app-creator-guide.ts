  "await AiPhone.voice.play({ dataUrl: speech.dataUrl });            // 主通道,播完 resolve",
  "AiPhone.voice.play({ channel: \"ambience\", dataUrl: bgmDataUrl, loop: true, volume: 0.3 }); // 循环环境音",
  "await AiPhone.voice.stopPlayback({ channel: \"voice\" });          // 停止",
  "await AiPhone.voice.pausePlayback({});                             // 暂停(默认 voice 通道)",
  "await AiPhone.voice.resumePlayback({});                            // 继续",
  "```",
  "",
  "* 两个通道：`voice`（默认，台词/提示音）和 `ambience`（循环背景音），互不打断；同通道新的播放会顶掉旧的。",
  "* `dataUrl` 也可以换成 `media.put` 返回的 `media-store://` 引用（推荐，见 5.10），此时音频数据完全不经过通信桥。",
  "* 播放需 `voice.tts` 权限。播放被浏览器拦截时会 reject，请 `try/catch` 做文字兜底。",
  "",
